//write a java program to display first ten natural numbers
public class FirstTenNaturalNumbers {
    public static void main(String[] args) {
        int n = 10; // number of natural numbers to display
        for (int i = 1; i <= n; i++) {
            System.out.println(i);
        }
    }
}
